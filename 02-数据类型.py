# int 整型
print(type(1))
# float 浮点型
print(type(1.1))
# str 字符串
print(type('hello python'))
# bool 布尔型
print(type(True))
# tuple 元组
print(type((1, 2, 3)))
# list 列表
print(type([1, 2, 3]))
# set 集合
print(type({1, 2, 3}))
# dict 字典
print(type({'name': 'jim', 'age': 18}))

